def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--stelnet", type=Path, default=None,
                    help="Path to the stelnet binary (default: build-ninja-compile/bin/stelnet)")
    args = ap.parse_args()
    stelnet = args.stelnet or find_stelnet()
    if not stelnet or not stelnet.is_file():
        print("error: stelnet binary not found. Build first or pass --stelnet.",
              file=sys.stderr)
        return 1
    backends, caps = load_backend_data(stelnet)
    DOCS_DIR.mkdir(parents=True, exist_ok=True)
    md = DOCS_DIR / "feature-matrix.md"
    html = DOCS_DIR / "feature-matrix.html"
    write_markdown(backends, caps, md)
    write_html(backends, caps, html)
    print(f"wrote {md.relative_to(REPO_ROOT)}  ({md.stat().st_size:,} bytes)")
    print(f"wrote {html.relative_to(REPO_ROOT)} ({html.stat().st_size:,} bytes)")
    print(f"  {len(backends)} backends · {len(caps)} caps")
    return 0


if __name__ == "__main__":
    sys.exit(main())
